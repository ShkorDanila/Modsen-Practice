// Попробуйте объявить переменную с тем же
// именем дважды, используя var, let и const.
// Какие переменные позволяют повторное
// объявление, а какие нет?

{
    var variable = 1
    const variable = 1
}

{
    let variable = 1
    const variable = 1
}

{
    let variable = 1
    var variable = 1
}

// В комбинации с var представляется возможным объявить переменные с одинакомыв именем дважды